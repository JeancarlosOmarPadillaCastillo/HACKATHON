
package modelo;


public class Sucursal {
    private int idsur;
    private String nomsur;
    private String dirsur;
    private String codubi;

    public int getIdsur() {
        return idsur;
    }

    public void setIdsur(int idsur) {
        this.idsur = idsur;
    }

    public String getNomsur() {
        return nomsur;
    }

    public void setNomsur(String nomsur) {
        this.nomsur = nomsur;
    }

    public String getDirsur() {
        return dirsur;
    }

    public void setDirsur(String dirsur) {
        this.dirsur = dirsur;
    }

    public String getCodubi() {
        return codubi;
    }

    public void setCodubi(String codubi) {
        this.codubi = codubi;
    }
}
